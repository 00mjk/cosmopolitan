#include "dsp/tty/quant.h"
#include "libc/bits/xmmintrin.h"

struct TtyRgb rgb2ttyf2i_(__m128 rgb) {
  __v4si i4;
  rgb *= 255;
  /* i4 = __builtin_ia32_cvtps2dq(rgb); */
  asm("cvttps2dq\t%0,%1" : "+%x"(rgb), "=x"(i4));
  return rgb2tty(i4[0], i4[1], i4[2]);
}
