#include "libc/intrin/pext.h"

/**
 * Parallel bit extract.
 */
uint64_t(pext)(uint64_t x, uint64_t mask) {
  uint64_t r, b;
  for (r = 0, b = 1; mask; mask >>= 1, x >>= 1) {
    if (mask & 1) {
      if (x & 1) r |= b;
      b <<= 1;
    }
  }
  return r;
}
